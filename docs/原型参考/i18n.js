// translate
window.t = function (key, ...args) {
  const dict = window.DICT[window.__lang] || window.DICT.zh;
  const en   = window.DICT.en;
  const v = dict[key] ?? en[key] ?? key;
  if (typeof v === "function") return v(...args);
  return v;
};

// React hook — re-renders subscribers when language changes
window.__langSubs = window.__langSubs || new Set();
window.useLang = function () {
  const [, force] = React.useReducer(x => x + 1, 0);
  React.useEffect(() => {
    window.__langSubs.add(force);
    return () => window.__langSubs.delete(force);
  }, []);
  return window.__lang;
};
window.setLang = function (lang) {
  window.__lang = lang;
  window.__langSubs.forEach(fn => fn());
  document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";
};
